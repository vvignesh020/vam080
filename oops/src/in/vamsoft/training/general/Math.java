package in.vamsoft.training.general;


public class Math {

  /**
   * @param a
   *          integer value passed.
   * @param b
   *          integer value passed.
   * @return it returns the sum of integer value.
   */
  public int sum(int a, int b) {
    return (a + b);
  }

  /**
   * @param a
   *          double value passed.
   * @param b
   *          double value passed.
   * @return it returns the sum of double value.
   */
  public double sum(double a, double b) {
    return (a + b);
  }

}
